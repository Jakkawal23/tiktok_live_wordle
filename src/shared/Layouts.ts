/**
 * groupArr
 * แบ่ง array ขนาดใหญ่เป็น array ย่อยตามจำนวนที่กำหนด
 * @param data - array ของตัวอักษร
 * @param n - จำนวนตัวอักษรในแต่ละกลุ่ม
 * @returns array ของ array ย่อย
 */
function groupArr<T>(data: T[], n: number): T[][] {
  const group: T[][] = [];
  for (let i = 0, j = 0; i < data.length; i++) {
    if (i >= n && i % n === 0) j++;
    group[j] = group[j] || [];
    group[j].push(data[i]);
  }
  return group;
}

